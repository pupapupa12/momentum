const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  square: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(3, 5);
calculator.minus(19, 5);
calculator.divide(8, 2);
calculator.square(5, 2);
